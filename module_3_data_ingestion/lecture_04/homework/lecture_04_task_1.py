products = ["Яблоки", "Хлеб", "Молоко", "Печенье", "Сок", "Кефир"]

#Выборочная проверка
for i in range(0, len(products), 2):
    item = products[i]
    print(f'Индекс {i}: Проверен товар {item} (Длина названия: {len(item)} символов)')

    if item == 'Бананы':
        print('Обнаружены бананы')
        break
else:
    print('--- Выборочная проверка успешно завершена ---')


def calculate_purchase(product_name, weight, price):
    '''
    Функция для безопасного расчета стоимости партии товара.
    :param product_name: Название товара
    :param weight: Вес партии (может быть в виде строки, числа или списка)
    :param price: Цена за килограмм
    Функция пытается преобразовать вес в число, рассчитать стоимость и технический индекс.
    В случае ошибок выводит тип и сообщение об ошибке.
    Всегда выводит сообщение о завершении проверки.
    '''

    try:
        #попытка преобразовать вес
        numeric_weight = float(weight)

        #рассчитываем полную стоимость партии
        total_cost = numeric_weight * price

        #рассчитываем индекс распределения
        technical_index = 100 / numeric_weight

        # Если все прошло успешно, выводим результат
        print(f"Товар: {product_name}. Итоговая стоимость: {total_cost}$")
    except TypeError as e:
        # Ошибка: неподходящий тип данных
        print(f"Тип ошибки: {type(e)}")
        print(f"Сообщение: {e}")
    except ValueError as e:
        # Ошибка: невозможно преобразовать текст в число
        print(f"Тип ошибки: {type(e)}")
        print(f"Сообщение: {e}")
    except ZeroDivisionError as e:
        # Ошибка: нельзя делить на ноль
        print(f"Тип ошибки: {type(e)}")
        print(f"Сообщение: {e}")
    finally:
        print('--- Проверка партии завершена ---')


# Проверка работы функции с разными данными

# 1. Корректные данные
calculate_purchase("Томаты", "100", 2.5)

print()  # Пустая строка для разделения

# 2. Ошибка значения - текст вместо числа
calculate_purchase("Огурцы", "пятьдесят", 1.8)

print()

# 3. Ошибка деления на ноль
calculate_purchase("Перец", "0", 4.0)

print()

# 4. Ошибка типа данных - список вместо числа
calculate_purchase("Зелень", [10], 5.0)